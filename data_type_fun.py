# Store the string "42" in the variable 'number'
number = "42"

# Print the string value
print(number)

# Print the data type of 'number' (should be <class 'str'>)
print(type(number))

# Convert the string "42" into an integer 42
int_number = int(number)

# Print the integer value
print(int_number)

# Print the data type of 'int_number' (should be <class 'int'>)
print(type(int_number))

# Add 5 to the integer value (numeric addition: 42 + 5 = 47)
print(int_number + 5)

# Concatenate the string "42" with the string "5" (string concatenation: "425")
print(number + "5")
